#include <iostream>
using namespace std;
int main() {
    cout << "Limits of Integer Data Types\n\n";
    cout << "short int : -32768 to 32767" << endl;
    cout << "int : -2147483648 to 2147483647" << endl;
    cout << "long int : -2147483648 to 2147483647" << endl;
    cout << "long long int : -9223372036854775808 to 9223372036854775807" << endl;
    cout << "\nUnsigned types:\n";
    cout << "unsigned short : 0 to 65535" << endl;
    cout << "unsigned int : 0 to 4294967295" << endl;
    cout << "unsigned long long : 0 to 18446744073709551615" << endl;
    return 0;
}